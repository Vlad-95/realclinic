function fadeIn(el, timeout = 300, display = 'block') {
  el.style.opacity = 0;
  el.style.display = display;
  el.style.transition = `opacity ${timeout}ms`;

  setTimeout(() => {
    el.style.opacity = 1;
  }, 10);
}

function fadeOut(el, timeout = 300) {
  el.style.opacity = 1;
  el.style.transition = `opacity ${timeout}ms`;
  el.style.opacity = 0;

  setTimeout(() => {
    el.style.display = 'none';
  }, timeout);
}

export { fadeIn, fadeOut };
